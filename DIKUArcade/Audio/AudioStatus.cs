namespace DIKUArcade.Audio;

/// <summary>
/// enum for the different possible audio statuses.
/// </summary>
public enum AudioStatus {
    /// <summary>
    /// Audio status if the audio is playing.
    /// </summary>
    Playing,

    /// <summary>
    /// Audio status if the audio is paused.
    /// </summary>
    Paused,

    /// <summary>
    /// Audio status if the audio is stopped.
    /// </summary>
    Stopped
}
